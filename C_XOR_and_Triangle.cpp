void solve()
{
    int n;
    cin>>n;
    if ((n & (n-1)) == 0){
        cout<<-1;
        return;
    }
    else if (((n+1) & n) == 0){
        cout<<-1;
        return;
    }
 
    int size = 32 - __builtin_clz(n);
    bitset<32> bits(n);
    bits[size -1] = 0;
    for(int i = size-2; i >= 0; i--){
        if (bits[i] == 0){
            bits[i] = 1;
            break;
        }
    }
 
    cout<<bits.to_ullong();
 
}
 
